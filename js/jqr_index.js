$("document").ready(function() {

    $("#sort").change(function() {

        if ($("#sort option:selected").val() == "ASC") {
            $("#listProduct").addClass("hidden");
            $("#listProduct1").removeClass("hidden");
            $("#listProduct2").addClass("hidden");
        }

        if ($("#sort option:selected").val() == "DES") {
            $("#listProduct1").addClass("hidden");
            $("#listProduct").addClass("hidden");
            $("#listProduct2").removeClass("hidden");
        }
    })
})